    public Chart(JPanel chartPanel, ArrayList<String> categorien, ArrayList<Double> bedragen) {
        this();
        chartPanel.setPreferredSize(new Dimension(550, 500));
        pnlChart.getViewport().removeAll();
        pnlChart.getViewport().add(chartPanel);
        StringBuilder builder = new StringBuilder();
        for (int i = 0; i < categorien.size(); i++) {
            builder.append(categorien.get(i));
            builder.append(" - ");
            builder.append("\u20ac");
            builder.append(deciForm.format(bedragen.get(i)));
            builder.append("\n\n");
        }
        txtChart.setText(builder.toString());
    }

